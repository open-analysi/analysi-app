@pytest.mark.integration
class TestEnrichAlertFunction:
    """Integration tests for enrich_alert Cy function."""

    @pytest.mark.asyncio
    async def test_enrich_alert_adds_data_under_cy_name(
        self, integration_test_session: AsyncSession
    ):
        """Verify enrich_alert adds enrichments keyed by task's cy_name."""
        tenant_id = f"test-enrich-{uuid4().hex[:8]}"

        cy_script = """
alert = input

# Add simple enrichment data
enrichment_data = {
    "source": "test_task",
    "score": 85,
    "details": "This is a test enrichment"
}

result = enrich_alert(alert, enrichment_data)
return result
"""
        input_alert = {"title": "Test Alert", "severity": "high"}

        _task, task_run = await _create_task_and_run(
            integration_test_session,
            tenant_id,
            task_name="Simple Enrichment Task",
            cy_name="simple_enrichment",
            cy_script=cy_script,
            input_data=input_alert,
        )

        service = TaskExecutionService()
        result = await service.execute_single_task(task_run.id, tenant_id)

        assert result.status == TaskExecutionStatus.COMPLETED, (
            f"Expected COMPLETED, got {result.status}: {result.error_message}"
        )

        output_dict = parse_cy_output(result.output_data)
        assert isinstance(output_dict, dict)

        # Verify enrichments structure
        assert "enrichments" in output_dict
        enrichments = output_dict["enrichments"]

        # Key insight: enrich_alert uses cy_name from execution context
        # The cy_name is set when task execution starts
        assert "simple_enrichment" in enrichments, (
            f"Expected 'simple_enrichment' in enrichments, got: {list(enrichments.keys())}"
        )

        enrichment = enrichments["simple_enrichment"]
        assert enrichment["source"] == "test_task"
        assert enrichment["score"] == 85

        # Original alert data preserved
        assert output_dict["title"] == "Test Alert"
        assert output_dict["severity"] == "high"
