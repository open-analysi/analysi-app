class UploadFileAction(IntegrationAction):
    """Upload file to Slack"""

    async def execute(self, **kwargs) -> dict[str, Any]:  # noqa: C901
        """Upload file to Slack

        Args:
            **kwargs: Must contain:
                - destination (string): Channel (e.g. #channel or C1A1A1AAA) or user (e.g. @user or U1A1A1AAA) to upload to
                - parent_message_ts (string): Parent message timestamp to reply in thread
                - file (string): Vault ID of file to upload
                - content (string): Contents of the file
                - caption (string): Caption to add to the file
                - filetype (string): A file type identifier (https://api.slack.com/types/file#file_types)
                - filename (string): Name of the file

        Returns:
            Result with status and data or error"""
        # Extract parameters
        destination = kwargs.get("destination")
        parent_message_ts = kwargs.get("parent_message_ts")
        file = kwargs.get("file")
        content = kwargs.get("content")
        caption = kwargs.get("caption")
        filetype = kwargs.get("filetype")
        filename = kwargs.get("filename")

        # Validate required parameters
        if not destination:
            return {
                "status": "error",
                "error": "Missing required parameter: destination",
                "error_type": "ValidationError",
            }

        # Retrieve credentials
        bot_token = self.credentials.get("bot_token")
        if not bot_token:
            return {
                "status": "error",
                "error": "Missing bot_token in credentials",
                "error_type": "ConfigurationError",
            }

        # Retrieve settings
        timeout = self.settings.get("timeout", 30)

        try:
            # Validate that either a file path or content is provided
            if not file and not content:
                return {
                    "status": "error",
                    "error": "Either 'file' (path) or 'content' must be provided",
                    "error_type": "ValidationError",
                }

            endpoint = "https://slack.com/api/files.upload"

            headers = {
                "Authorization": f"Bearer {bot_token}",
            }

            data: dict[str, Any] = {
                "channels": destination,
            }
            if caption:
                data["initial_comment"] = caption
            if filetype:
                data["filetype"] = filetype
            if filename:
                data["filename"] = filename
            if parent_message_ts:
                data["thread_ts"] = parent_message_ts

            files_payload = None
            file_handle = None

            if file:
                # Security: validate the file path to prevent path traversal.
                # Resolve symlinks and ".." components, then verify the
                # canonical path is genuinely *inside* an allowed directory.
                # We use Path.is_relative_to (a real path-ancestry check) — a
                # naive str.startswith would accept sibling directories that
                # share a prefix string, e.g. "/tmp_evil/" vs "/tmp/".
                try:
                    resolved = Path(file).resolve(strict=False)
                except (ValueError, OSError):
                    return {
                        "status": "error",
                        "error": "Invalid file path",
                        "error_type": "ValidationError",
                    }

                if not any(
                    resolved.is_relative_to(prefix) for prefix in _ALLOWED_FILE_PREFIXES
                ):
                    return {
                        "status": "error",
                        "error": "Path traversal blocked: file must be under an allowed directory",
                        "error_type": "ValidationError",
                    }

                file_path = str(resolved)
                if not os.path.isfile(file_path):
                    return {
                        "status": "error",
                        "error": f"File not found at path: {file_path}",
                        "error_type": "ValidationError",
                    }
                upload_filename = (
                    filename or os.path.basename(file_path) or "upload.bin"
                )
                file_handle = open(file_path, "rb")  # noqa: SIM115
                files_payload = {
                    "file": (upload_filename, file_handle, "application/octet-stream")
                }
            else:
                data["content"] = content

            async with httpx.AsyncClient(timeout=timeout) as client:
                response = await client.post(
                    endpoint, headers=headers, data=data, files=files_payload
                )
                response.raise_for_status()

            # Ensure file handle is closed after the request
            if file_handle:
                with contextlib.suppress(Exception):
                    file_handle.close()

            resp_json = response.json()
            if not resp_json.get("ok", False):
                return {
                    "status": "error",
                    "error": resp_json.get("error", "Slack API returned ok=false"),
                    "error_type": "SlackAPIError",
                }

            file_json = resp_json.get("file", {}) or {}

            # Build thumbnails structure and normalize some fields similar to upstream implementation
            thumbnail_dict: dict[str, dict[str, Any]] = {}
            pop_list = []
            for key, value in list(file_json.items()):
                if isinstance(key, str) and key.startswith("thumb"):
                    pop_list.append(key)
                    name_arr = key.split("_")
                    if len(name_arr) >= 2:
                        thumb_name = f"{name_arr[0]}_{name_arr[1]}"
                        if thumb_name not in thumbnail_dict:
                            thumbnail_dict[thumb_name] = {}
                        if len(name_arr) == 2:
                            thumbnail_dict[thumb_name]["img_url"] = value
                        elif len(name_arr) >= 3:
                            if name_arr[2] == "w":
                                thumbnail_dict[thumb_name]["width"] = value
                            elif name_arr[2] == "h":
                                thumbnail_dict[thumb_name]["height"] = value
                elif key == "initial_comment":
                    # Slack may return initial_comment as string or object
                    if isinstance(value, dict):
                        resp_json["caption"] = value.get("comment") or value.get("text")
                    else:
                        resp_json["caption"] = value
                    pop_list.append(key)
                elif key in ["channels", "ims", "groups"]:
                    if "destinations" not in resp_json:
                        resp_json["destinations"] = []
                    if isinstance(value, list):
                        resp_json["destinations"] += value
                    pop_list.append(key)
                elif key == "username":
                    pop_list.append(key)
                elif key == "user":
                    resp_json["sender"] = value
                    pop_list.append(key)

            for poppee in pop_list:
                file_json.pop(poppee, None)

            resp_json["thumbnails"] = thumbnail_dict

            result: dict[str, Any] = {
                "status": "success",
                "destination": destination,
                "file": file_json,
                "thumbnails": thumbnail_dict,
                "full_data": resp_json,
            }

            # Convenience fields
            if file_json:
                result["file_id"] = file_json.get("id")
                result["file_name"] = file_json.get("name") or (
                    filename if filename else None
                )
                result["permalink"] = file_json.get("permalink") or file_json.get(
                    "permalink_public"
                )

            return result

        except httpx.HTTPStatusError as e:
            return {
                "status": "error",
                "error": f"HTTP error occurred: {e!s}",
                "error_type": "HTTPStatusError",
            }
        except httpx.RequestError as e:
            return {
                "status": "error",
                "error": f"Request error occurred: {e!s}",
                "error_type": "RequestError",
            }
        except httpx.TimeoutException as e:
            return {
                "status": "error",
                "error": f"Request timed out: {e!s}",
                "error_type": "TimeoutException",
            }

