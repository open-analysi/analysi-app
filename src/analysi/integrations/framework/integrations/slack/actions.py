class GetHistoryAction(IntegrationAction):
    """Get conversation history from specific Slack channel"""

    async def execute(self, **kwargs) -> dict[str, Any]:
        """Get conversation history from specific Slack channel

        Args:
            **kwargs: Must contain:
                - channel_id (string): Unique ID of a Slack channel
                - message_ts (string): Message timestamp (e.g. 1234567890.123456)

        Returns:
            Result with status and data or error"""
        # Extract parameters
        channel_id = kwargs.get("channel_id")
        message_ts = kwargs.get("message_ts")

        # Validate required parameters
        if not channel_id:
            return {
                "status": "error",
                "error": "Missing required parameter: channel_id",
                "error_type": "ValidationError",
            }

        # Retrieve credentials
        bot_token = self.credentials.get("bot_token")
        if not bot_token:
            return {
                "status": "error",
                "error": "Missing bot_token in credentials",
                "error_type": "ConfigurationError",
            }

        # Retrieve settings
        timeout = self.settings.get("timeout", 30)

        try:
            base_url = "https://slack.com/api"
            headers = {
                "Authorization": f"Bearer {bot_token}",
                "Accept": "application/json",
            }

            if not message_ts:
                # Fetch channel conversation history
                endpoint_history = f"{base_url}/conversations.history"
                params_history = {"channel": channel_id}
                response_history = await self.http_request(
                    endpoint_history,
                    headers=headers,
                    params=params_history,
                    timeout=timeout,
                )
                history_data = response_history.json()

                if not history_data.get("ok", False):
                    slack_error = history_data.get(
                        "error", "Failed to fetch conversation history"
                    )
                    if slack_error == "channel_not_found":
                        return {
                            "status": "success",
                            "not_found": True,
                            "channel_id": channel_id,
                            "error_code": slack_error,
                        }
                    return {
                        "status": "error",
                        "error": slack_error,
                        "error_type": "SlackAPIError",
                        "full_data": history_data,
                    }

                messages = history_data.get("messages", [])
                timestamps = [m.get("ts") for m in messages if m.get("ts")]

                final_messages = []
                threads_raw = []

                endpoint_replies = f"{base_url}/conversations.replies"
                for ts in timestamps:
                    params_replies = {"channel": channel_id, "ts": ts}
                    response_replies = await self.http_request(
                        endpoint_replies,
                        headers=headers,
                        params=params_replies,
                        timeout=timeout,
                    )
                    replies_data = response_replies.json()

                    if not replies_data.get("ok", False):
                        slack_error = replies_data.get(
                            "error", "Failed to fetch thread history"
                        )
                        if slack_error in (
                            "channel_not_found",
                            "thread_not_found",
                        ):
                            return {
                                "status": "success",
                                "not_found": True,
                                "channel_id": channel_id,
                                "message_ts": ts,
                                "error_code": slack_error,
                            }
                        return {
                            "status": "error",
                            "error": slack_error,
                            "error_type": "SlackAPIError",
                            "full_data": replies_data,
                        }

                    threads_raw.append(replies_data)
                    final_messages.extend(replies_data.get("messages", []))

                return {
                    "status": "success",
                    "channel_id": channel_id,
                    "num_messages": len(final_messages),
                    "messages": final_messages,
                    "full_data": {"history": history_data, "threads": threads_raw},
                }
            # Fetch specific thread history using provided message_ts
            endpoint_replies = f"{base_url}/conversations.replies"
            params_replies = {"channel": channel_id, "ts": message_ts}
            response_replies = await self.http_request(
                endpoint_replies,
                headers=headers,
                params=params_replies,
                timeout=timeout,
            )
            replies_data = response_replies.json()

            if not replies_data.get("ok", False):
                slack_error = replies_data.get("error", "Thread not found")
                if slack_error in ("channel_not_found", "thread_not_found"):
                    return {
                        "status": "success",
                        "not_found": True,
                        "channel_id": channel_id,
                        "message_ts": message_ts,
                        "error_code": slack_error,
                    }
                return {
                    "status": "error",
                    "error": slack_error,
                    "error_type": "SlackAPIError",
                    "full_data": replies_data,
                }

            messages = replies_data.get("messages", [])

            return {
                "status": "success",
                "channel_id": channel_id,
                "message_ts": message_ts,
                "num_messages": len(messages),
                "messages": messages,
                "full_data": replies_data,
            }

        except httpx.HTTPStatusError as e:
            if e.response.status_code == 404:
                return {
                    "status": "success",
                    "not_found": True,
                    "channel_id": channel_id,
                    "message_ts": message_ts,
                }
            return {
                "status": "error",
                "error": f"HTTP error occurred: {e!s}",
                "error_type": "HTTPStatusError",
            }
        except httpx.RequestError as e:
            return {
                "status": "error",
                "error": f"Request error occurred: {e!s}",
                "error_type": "RequestError",
            }
        except httpx.TimeoutException as e:
            return {
                "status": "error",
                "error": f"Request timed out: {e!s}",
                "error_type": "TimeoutException",
            }
